module.exports = {
    calculateCRC16IBM(data) {
        let crc = 0x0000;
        let buffer = Buffer.from(data, 'hex');
        for (const byte of buffer) {
            crc = crc >>> 8 ^ crcIBMTable[(crc ^ byte) & 0xff];
        }
        crc = crc.toString(16);

        return crc;
    },

    calculateCRCITU(data) {
        let crc = parseInt('ffff', 16);
        let i = 0;
        while (i < data.length) {
            let str = data.substr(i, 2);
            str = parseInt(str, 16);
            crc = (crc >> 8) ^ crcITUTable[(crc ^ str) & 0xff];
            i = i + 2;
        }
        crc = crc ^ 0xffff;
        crc = crc.toString(16);

        return crc;
    }
}